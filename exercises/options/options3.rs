// hint.

struct Point {
    x: i32,
    y: i32,
}

fn main() {
    let y: Option<Point> = Some(Point { x: 100, y: 200 });

    match y {
        //在 Rust 中，关键字 ref 有两种主要用法：ref 和 ref mut。它们用于在模式匹配中创建对变量的引用，而不是将值移动或复制。
        Some(ref p) => println!("Co-ordinates are {},{} ", p.x, p.y),
        _ => panic!("no match!"),
    }
    y; // Fix without deleting this line.
}
